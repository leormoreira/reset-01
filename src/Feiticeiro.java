package tema1;

public class Feiticeiro {
    String nome;
    double vida;
    double ataque;
    double defesa;
    double mana;

    Feiticeiro (String nome, double vida, double ataque, double defesa, double mana) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.mana = mana;
    }

}
