package tema1;

public class Clerigo {
    String nome;
    double vida;
    double ataque;
    double defesa;
    double fe;

    Clerigo (String nome, double vida, double ataque, double defesa, double fe) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.fe = fe;
    }

}
